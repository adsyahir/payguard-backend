-- user-service schema (payguard_user) — merchants, users, roles, RBAC join, refresh tokens.
-- PKs are BIGINT identity (internal, fast, sequential).
-- users & merchants also carry a UUID (public-facing id — safe to expose, non-guessable).

-- Merchants an account belongs to.
CREATE TABLE merchants (
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    uuid          UUID NOT NULL DEFAULT gen_random_uuid(),
    legal_name    VARCHAR(255) NOT NULL,
    display_name  VARCHAR(255) NOT NULL,
    country       VARCHAR(2)   NOT NULL,                     -- ISO-3166 alpha-2
    risk_tier     VARCHAR(10)  NOT NULL DEFAULT 'low',
    status        VARCHAR(20)  NOT NULL DEFAULT 'pending',
    created_at    TIMESTAMPTZ  NOT NULL DEFAULT now(),
    CONSTRAINT chk_merchant_risk_tier CHECK (risk_tier IN ('low', 'medium', 'high')),
    CONSTRAINT chk_merchant_status    CHECK (status IN ('pending', 'active', 'suspended'))
);

-- fast public-id lookups (and enforces uniqueness)
CREATE UNIQUE INDEX idx_merchants_uuid ON merchants (uuid);

-- Users who sign in (belong to a merchant).
CREATE TABLE users (
    id             BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    uuid           UUID NOT NULL DEFAULT gen_random_uuid(),
    merchant_id    BIGINT NOT NULL REFERENCES merchants (id) ON DELETE CASCADE,
    email          VARCHAR(255) NOT NULL UNIQUE,
    password_hash  VARCHAR(255) NOT NULL,                    -- BCrypt hash, never plaintext
    full_name      VARCHAR(255) NOT NULL,
    is_active      BOOLEAN      NOT NULL DEFAULT true,
    created_at     TIMESTAMPTZ  NOT NULL DEFAULT now()
);

-- fast public-id lookups (and enforces uniqueness)
CREATE UNIQUE INDEX idx_users_uuid ON users (uuid);
CREATE INDEX idx_users_merchant_id ON users (merchant_id);

-- RBAC roles.
CREATE TABLE roles (
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name         VARCHAR(50) NOT NULL UNIQUE,
    description  VARCHAR(255)
);

-- User <-> Role many-to-many.
CREATE TABLE user_roles (
    user_id  BIGINT NOT NULL REFERENCES users (id) ON DELETE CASCADE,
    role_id  BIGINT NOT NULL REFERENCES roles (id) ON DELETE CASCADE,
    PRIMARY KEY (user_id, role_id)
);

-- Fine-grained permissions (e.g. payment:read, fraud:review, user:manage).
CREATE TABLE permissions (
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name         VARCHAR(100) NOT NULL UNIQUE,
    description  VARCHAR(255)
);

-- Role <-> Permission many-to-many.
CREATE TABLE role_permissions (
    role_id        BIGINT NOT NULL REFERENCES roles (id) ON DELETE CASCADE,
    permission_id  BIGINT NOT NULL REFERENCES permissions (id) ON DELETE CASCADE,
    PRIMARY KEY (role_id, permission_id)
);

-- Refresh tokens (long-lived, revocable). Access tokens (JWT) are stateless and NOT stored.
CREATE TABLE refresh_tokens (
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id      BIGINT NOT NULL REFERENCES users (id) ON DELETE CASCADE,
    token_hash   VARCHAR(255) NOT NULL UNIQUE,               -- SHA-256 of the raw token; raw is never stored
    expires_at   TIMESTAMPTZ  NOT NULL,
    revoked      BOOLEAN      NOT NULL DEFAULT false,
    replaced_by  BIGINT REFERENCES refresh_tokens (id),      -- set on rotation (points at the new token)
    created_at   TIMESTAMPTZ  NOT NULL DEFAULT now()
);

CREATE INDEX idx_refresh_tokens_user_id ON refresh_tokens (user_id);
